/** Conteo por sección, ordenado por cantidad descendente. */
export function porSeccion(rows: NominaRegistro[]): { seccion: string; cantidad: number }[] {
  const m = new Map<string, number>();
  for (const r of rows) m.set(r.seccion, (m.get(r.seccion) ?? 0) + 1);
  return Array.from(m.entries())
    .map(([seccion, cantidad]) => ({ seccion, cantidad }))
    .sort((a, b) => b.cantidad - a.cantidad);
}

/** Conteo por modalidad. */
export function porModalidad(rows: NominaRegistro[]): { modalidad: string; cantidad: number }[] {
  const m = new Map<string, number>();
  for (const r of rows) m.set(r.modalidad, (m.get(r.modalidad) ?? 0) + 1);
  return Array.from(m.entries())
    .map(([modalidad, cantidad]) => ({ modalidad, cantidad }))
    .sort((a, b) => b.cantidad - a.cantidad);
}

/** Conteo combinado sección × modalidad. */
export function porSeccionYModalidad(rows: NominaRegistro[]) {
  const m = new Map<string, Map<string, number>>();
  for (const r of rows) {
    if (!m.has(r.seccion)) m.set(r.seccion, new Map());
    const sub = m.get(r.seccion)!;
    sub.set(r.modalidad, (sub.get(r.modalidad) ?? 0) + 1);
  }
  return Array.from(m.entries()).map(([seccion, mods]) => ({
    seccion,
    total: Array.from(mods.values()).reduce((a, b) => a + b, 0),
    modalidades: Array.from(mods.entries())
      .map(([modalidad, cantidad]) => ({ modalidad, cantidad }))
      .sort((a, b) => b.cantidad - a.cantidad),
  })).sort((a, b) => b.total - a.total);
}
